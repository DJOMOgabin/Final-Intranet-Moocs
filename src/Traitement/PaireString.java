package Traitement;

public class PaireString {

	private String nom,valeur;
	
	public PaireString(String nom,String valuer){
		this.nom=nom;
		this.valeur=valuer;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getValeur() {
		return valeur;
	}

	public void setValeur(String valeur) {
		this.valeur = valeur;
	}
	
}
